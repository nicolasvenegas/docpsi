                  <div>
                    <h3 class="text-center"><b>1.1. Primera Etapa de Postulación</b></h3>
                    <p class="text-center">
                    <b>1. </b><a href="#">Formulario de postulación</a>.
                    <b>2. </b>Certificado original o copia legalizada (i) del grado de licenciado y/o magíster, (ii) del o los certificados de concentración de notas correspondientes, y (iii) del ranking de egreso del postulante en el programa de licenciatura y/o magíster en que se graduó.
                    <b>3. </b>Curriculum Vitae actualizado.
                    <b>4. </b>Declaración de propósito que explicite (i) la motivación del postulante por ingresar al programa, (ii) cómo tal motivación es coherente con su formación previa, (iii) cómo el programa puede satisfacer tal motivación, y (iv) sus planes laborales una vez obtenido el grado doctoral (máximo una carilla tamaño carta).
                    <b>5. </b>Una propuesta de investigación consistente en una breve elaboración conceptual y metodológica del tema que el postulante investigaría como posible trabajo de tesis, incluyendo el nombre del profesor del programa que podría guiar su tesis (máximo dos carillas tamaño carta).
                    <b>6. </b><a href="#">Dos cartas de recomendación según formulario estándar</a>, emitidas por doctores, al menos uno de ellos en Psicología. Además, al menos una de las cartas deberá ser de un académico de la universidad donde se tituló o graduó el postulante, o bien de la universidad donde actualmente.
                    <b>7. </b><a href="#">Carta de compromiso del postulante</a>, asegurando una dedicación exclusiva al programa acompañada, cuando corresponda, de un documento de patrocinio o autorización de la institución a la cual pertenece (disponible en web).
                    <b>8. </b>Documento oficial con resultados que demuestren el nivel de dominio oral y escrito del castellano del postulante (exigible sólo a postulantes procedentes de países con idiomas distintos al castellano).
                    </p>
