          <h5 style="margin: 6rem 0 1rem;">Arancel</h5>
          <p><small>El costo del arancel anual del programa es $3.700.000, pagaderos en 10 cuotas mensuales de $370.000 en cada uno de los cuatro años que dura el programa. En los casos en que el estudiante se adjudique una beca ANID (ex-CONICYT) o equivalente y el monto de la beca sea inferior al arancel, la Universidad liberará al estudiante del pago de dicha diferencia. En el mes de marzo de cada año se debe cancelar un arancel de matrícula cuyo monto para el ingreso 2025 es de $350.000.</small></p>
