              <li>El Programa dispone de Becas otorgadas por la Universidad de La Frontera, la cual libera del arancel de pagos y otorga una manutención de $450.000 mensuales.</li>
              <li>El Programa cuenta con becas internas, la cual consiste en la liberación del arancel mensual, pudiendo liberar hasta el 100% del costo anual del programa (se estudia caso a caso).</li>
              <li>El Programa en casos especiales, puede suplementar la beca de manutención de la Universidad de La Frontera.</li>
              <li>Existe la posibilidad de incorporarse a Líneas de Investigación con Proyectos de Investigación vigentes, los cuales pueden subsidiar parcialmente estudios de doctorado.</li>
