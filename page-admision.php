                    <h3 class="text-center"><b>0. Criterios de Selección</b></h3>
                    <p class="text-center">
                      <b>1. </b>Rendimiento académico en la obtención del Grado de Licenciado y/o Magister en Psicología o área afín.
                      <b>2. </b>Competencia en estadística y métodos de investigación correspondientes a la formación de pregrado en psicología.
                      <b>3. </b>Competencia en lectura comprensiva del idioma inglés.
                      <b>4. </b>Participación en proyectos de investigación (deseable).
                      <b>5. </b>Participación en publicaciones científicas (deseable).
                      <b>6. </b>Experiencia docente universitaria (ayudantías, supersiones, docencia).
                      <b>7. </b>Calidad de la propuesta de investigación.
                      <b>8. </b>Coherencia y claridad de propósitos en el interés por ingresar al programa.
                      <b>9. </b>Competencia oral y escrita en castellano, sólo en aquellos postulantes que procedan de países con idiomas distintos al castellano.
                    </p>
